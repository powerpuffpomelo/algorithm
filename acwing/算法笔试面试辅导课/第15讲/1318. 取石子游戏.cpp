// 博弈论入门
// 状态：必胜态 & 必败态
// 如果可以变成必败 则必胜；如果只能变成必胜 则必败
#include <bits/stdc++.h>
using namespace std;

int n, k;

int main(){
    cin >> n >> k;
    if(n % (k + 1)) puts("1");
    else puts("2");
    return 0;
}